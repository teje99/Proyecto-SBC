spi_device_handle_t oled_handle;

// Función para enviar un comando
void oled_send_command(uint8_t cmd) {
    spi_transaction_t trans = {
        .length = 8, // 8 bits
        .tx_buffer = &cmd,
    };

    gpio_set_level(PIN_NUM_DC, 0); // Modo comando
    esp_err_t ret = spi_device_transmit(oled_handle, &trans);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Error enviando comando: %s", esp_err_to_name(ret));
    }
}

// Función para enviar datos
void oled_send_data(uint8_t data) {
    spi_transaction_t trans = {
        .length = 8, // 8 bits
        .tx_buffer = &data,
    };

    gpio_set_level(PIN_NUM_DC, 1); // Modo datos
    esp_err_t ret = spi_device_transmit(oled_handle, &trans);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Error enviando datos: %s", esp_err_to_name(ret));
    }
}

// Inicialización del OLED
void oled_init(void) {
    spi_bus_config_t buscfg = {
        .miso_io_num = -1,
        .mosi_io_num = PIN_NUM_MOSI,
        .sclk_io_num = PIN_NUM_SCLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = 0,
    };

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 10 * 1000 * 1000, // Velocidad del bus SPI
        .mode = 0,                          // Modo SPI
        .spics_io_num = PIN_NUM_CS,         // GPIO CS
        .queue_size = 1,
    };

    // Inicializar el bus SPI
    ESP_ERROR_CHECK(spi_bus_initialize(SPI2_HOST, &buscfg, SPI_DMA_CH_AUTO));

    // Agregar el dispositivo OLED al bus SPI
    ESP_ERROR_CHECK(spi_bus_add_device(SPI2_HOST, &devcfg, &oled_handle));

    // Configurar los pines DC y RES
    gpio_config_t io_conf = {
        .pin_bit_mask = (1ULL << PIN_NUM_DC) | (1ULL << PIN_NUM_RES),	// 
        .mode = GPIO_MODE_OUTPUT,
    };
    gpio_config(&io_conf);

    // Reset del OLED
    gpio_set_level(PIN_NUM_RES, 0);
    vTaskDelay(pdMS_TO_TICKS(100));
    gpio_set_level(PIN_NUM_RES, 1);

    // Secuencia de inicialización del SSD1331
    oled_send_command(0xAE); // Apagar pantalla
    oled_send_command(0xA0); // Configuración remap
    oled_send_command(0x72); // Color de barrido RGB
    oled_send_command(0xA1); // Configuración offset
    oled_send_command(0x00);
    oled_send_command(0xA2); // Configuración de línea de inicio
    oled_send_command(0x00);
    oled_send_command(0xA4); // Modo de visualización normal
    vTaskDelay(100/portTICK_PERIOD_MS);
    oled_send_command(0xAF); // Encender pantalla
}

void oled_draw_pixel(uint8_t x, uint8_t y, uint16_t color) {
    if (x >= 96 || y >= 64) return; // Fuera de rango

    oled_send_command(0x15); // Establecer columna
    oled_send_command(x);
    oled_send_command(95);

    oled_send_command(0x75); // Establecer fila
    oled_send_command(y);
    oled_send_command(63);

    oled_send_data(color >> 8); // Enviar color (parte alta)
    oled_send_data(color & 0xFF); // Enviar color (parte baja)
}

void oled_clear(uint16_t color) {
    for (uint8_t y = 0; y < 64; y++) {
        for (uint8_t x = 0; x < 96; x++) {
            oled_draw_pixel(x, y, color);
        }
    }
}

void write_byte(int x, int y, uint8_t byte_row)		// posición de la esquina superior izq de la posición de la letra.
{
	for ( int i = 0; i<8; i++){
		if (((byte_row>>i)&0x01) == 1)
		oled_draw_pixel(x+i, y, BLANCO);
	}
}

void write_char(uint8_t x, uint8_t y, char c)
{

	for (int i = 0; i<8; i++)
	{
		write_byte(x, y+i, font8x8_basic[(uint8_t)c][i]);
	}

}


void write_line(uint8_t nLinea, char *texto, int valor)
{
	char line[12];
	sprintf(line, "%s%d", texto, valor);
		
	for (uint8_t y = 8*nLinea; y < 8*nLinea + 8; y++) {
        for (uint8_t x = 0; x < 96; x++) {
            oled_draw_pixel(x, y, NEGRO);
        }
    }
	
	for (int i = 0; i<strlen(line); i++)
	{
		write_char(i*8, 8*nLinea, line[i]);
	}
	
}
